package com.ibrahimtahasanli.mlkit

import java.util.*

class Angles {
    constructor(leftLegDeg:Float, rightLegDeg: Float, leftArmDeg: Float, rightArmDeg: Float, leftShoulderDeg: Float, rightShoulderDeg: Float, leftHipDeg: Float, rightHipDeg: Float) {
        this.leftLegDeg = leftLegDeg
        this.rightLegDeg = rightLegDeg
        this.leftArmDeg = leftArmDeg
        this.rightArmDeg = rightArmDeg
        this.leftShoulderDeg = leftShoulderDeg
        this.rightShoulderDeg = rightShoulderDeg
        this.leftHipDeg = leftHipDeg
        this.rightHipDeg = rightHipDeg
    }

    public var leftLegDeg: Float = 0f;
    public var rightLegDeg: Float = 0f;
    public var leftArmDeg: Float = 0f;
    public var rightArmDeg: Float = 0f;
    public var leftShoulderDeg: Float = 0f;
    public var rightShoulderDeg: Float = 0f;
    public var leftHipDeg: Float = 0f;
    public var rightHipDeg: Float = 0f;
}